package kr.or.iei.model;

//id, pwd, name
public class User {
	//필드
	String id;
	String pwd;
	String name;
	
	//생성자
	public User(String id, String pwd, String name) {
		this.id = id;
		this.pwd = pwd;
		this.name = name;
	}
	
	//getter
	public String getId() {
		return id;
	}
	
	public String getPwd() {
		return pwd;
	}
	
	public String getName() {
		return name;
	}
	
	//toString
	@Override
	public String toString() {
		return id + ", " + pwd + ", " + name;
	}
}
